package EmpiresMod.entities.Permissions;

//Permission Level from Empires-Permissions

public enum PermissionLevel {

	/**
	 * This permission is allowed by the container
	 */
	ALLOWED,

	/**
	 * This permission is denied by the container
	 */
	DENIED,

	/**
	 * Nothing specified in the container
	 */
	NONE
}
